											<p>Do komfortowego udziału w spływie kajakowym zalecamy przygotowanie stroju sportowego. W ciepłe dni, optymalnym zestawem ubrań będą krótkie spodenki i koszulka. Na chłodniejsze dni można zastąpić je spodniami sportowymi i bluzą z długim rękawem. Obowiązkowo na czas trwania spływu należy posiadać obuwie – specjalistyczne obuwie do wody, tenisówki lub sandały. Po zakończonym spływie uczestnicy będą mogli skorzystać z przebieralni dlatego też zalecamy zabranie ze sobą ręcznika, odzieży i bielizny na przebranie.</p>
